public class Pixel {
    private int x,y;

    public Pixel(int xValue, int yValue) {
        x = xValue;
        y = yValue;
    }

    public Pixel() {

    }

    public void setX(int xValue) {
        x = xValue;
    }

    public int getX( ) {
        return x;
    }

    public void setY(int yValue) {
        y = yValue;
    }

    public int getY( ) {
        return y;
    }
}
